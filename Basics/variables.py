# Variables in Python
"""A variable in Python is a named container used to store data values
 that can change during program execution."""
# Integer variable
age = 21

# Float variable
height = 5.9

# String variable
char="a"
name = "Harshad"

# Boolean variable (True or False)
is_student = True

# Displaying the variables
#"print" function is used to display any data in python
print("Name:", name)
print("Age:", age)
print("Height:", height)
print("Character:",char)
print("Student:", is_student)

# Doing some operations
next_year_age = age + 1
print("Age next year will be:", next_year_age)
